async def load_default_knowledge(storage):
    """Загрузить начальную базу знаний в Supabase."""
    existing = await storage.get_knowledge()
    if existing:
        return  # Уже загружено

    for item in DEFAULT_KNOWLEDGE:
        await storage.add_knowledge(
            category=item["category"],
            title=item["title"],
            content=item["content"],
            source=item.get("source", ""),
        )

    print(f"✅ Загружено {len(DEFAULT_KNOWLEDGE)} записей в базу знаний")
