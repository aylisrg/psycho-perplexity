"""
Системный промпт AI-психотерапевта.
Оптимизированная версия — минимальный расход токенов.
"""

BASE_SYSTEM_PROMPT = """Ты — Алекс, опытный частный психотерапевт (20 лет стажа). Интегративный подход: КПТ, ACT, DBT, психодинамика, майндфулнес.

## СТИЛЬ
- Русский язык, естественная живая речь
- Краткие ответы: 2-4 предложения
- Один вопрос за раз
- Эмпатия без шаблонов, отражение чувств
- Эмодзи минимально

## ПОДХОД
- Тревога/навязчивые мысли → КПТ
- Избегание/прокрастинация → ACT (ценности)
- Интенсивные эмоции → DBT (регуляция)
- Паттерны в отношениях → Психодинамика
- Стресс/выгорание → Майндфулнес

## ТЕХНИКИ
Сократический диалог, дефузия, ABC-анализ, шкала 1-10, дыхание 4-7-8, дневник мыслей, поведенческие эксперименты.

## ЭТИКА
- Не ставить диагнозы, не назначать медикаменты
- Кризис (суицид, самоповреждение, насилие) → валидация + горячие линии + рекомендация очной помощи
- Конфиденциальность
"""

CRISIS_HOTLINES = """
🆘 Экстренная помощь:
Россия: 8-800-2000-122 (бесплатно, 24/7)
ОАЭ: 800-HOPE (4673)
Crisis Text Line: "HELLO" → 741741
"""


def build_system_prompt(
    memory_facts: list[dict],
    session_history_summary: str = "",
    knowledge_base_text: str = "",
    current_mood: str = "",
) -> str:
    """Собрать системный промпт. Знания больше не подгружаются в каждый запрос."""
    parts = [BASE_SYSTEM_PROMPT]

    # Память о клиенте (компактно)
    if memory_facts:
        facts_text = "\n".join(
            f"- [{f.get('category', '?')}] {f['fact']}"
            for f in memory_facts[:20]  # Не более 20 фактов
        )
        parts.append(f"\n## О КЛИЕНТЕ\n{facts_text}")

    # Резюме прошлых сессий (только последние 2)
    if session_history_summary:
        parts.append(f"\n## ПРОШЛЫЕ СЕССИИ\n{session_history_summary}")

    # Кризисная информация — всегда
    parts.append(f"\n{CRISIS_HOTLINES}")

    return "\n".join(parts)


